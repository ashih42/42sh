#include "libft.h"

t_bool	ft_prime(long long n)
{
	long long i;

	if (n <= 1 || (n > 2 && n % 2 == 0))
		return (0);
	if (n <= 3)
		return (1);
	i = 3;
	while (i < n / 2)
	{
		if (n % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
